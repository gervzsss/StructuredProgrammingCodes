class LecStringNnumbers {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        System.out.println(a + b); // Outputs 30

        String d = "10";
        String e = "20";
        System.out.println(d + e); // Outputs 1020

        String x = "10";
        int y = 20;
        System.out.println(x + y); // z will be 1020 (a String)

    }
}
